            layout="->,total, sizes,  prev, pager, next, jumper"
            v-model:current-page="page.current"
            :page-sizes="[10, 20, 50, 100]"
            v-model:page-size="page.size"
            :total="page.total"
            @size-change="fetch(1)"
            @current-change="handleCurrent"
          >
          </el-pagination>
        </div>
      </div>
    </div>
    <SelectClassify
      v-if="classify"
      ref="classify"
      :types="classify.types"
      @operationsClassify="$emit('classify-submit', $event)"
    ></SelectClassify>
  </div>
</template>

<script>
import { $on, $off, $once, $emit } from '../utils/gogocodeTransfer'
import { delayTrigger } from '@tap/shared'
import { VIcon, Classification, ProTable } from '@tap/component'

import SelectClassify from './SelectClassify'

export default {
  components: {
    Classification,
    SelectClassify,
    VIcon,
    ProTable
  },
  props: {
    title: String,
    desc: String,
    defaultPageSize: {
      type: Number,
      default: 20
    },
    hideClassify: {
      // 是否隐藏左侧栏
      type: Boolean,
      default: false
    },
    classify: {
      type: Object
    },
    remoteMethod: Function,
    rowKey: [String, Function],
    spanMethod: [Function],
    defaultSort: Object,
    draggable: Boolean
  },
  data() {
    return {
      loading: false,
      page: {
        current: 1,
        size: this.defaultPageSize,
        total: 0
      },
      list: [],
      multipleSelection: [],
      tags: [],
      classifyDialogVisible: false
    }
  },
  mounted() {
    this.fetch(1)
  },
  methods: {
    fetch(pageNum, debounce = 0, hideLoading, callback) {
      let timer = null
      if (pageNum === 1) {
        this.multipleSelection = []
        $emit(this, 'selection-change', [])
        this.$refs?.table?.clearSelection()
      }
      this.page.current = pageNum || this.page.current
      this.$nextTick(() => {
        delayTrigger(() => {
          if (!hideLoading) {
            this.loading = true
          }
          this.remoteMethod &&
            this.remoteMethod({
              page: this.page,
              tags: this.tags,
              data: this.list
            })
              .then(({ data, total }) => {
                this.page.total = total
                this.list = data || []

                // 缓存每页条数
                let pageData = {}
                pageData[this.$route.name] = this.page.size

                if (total > 0 && (!data || !data.length)) {
                  clearTimeout(timer)
                  timer = setTimeout(() => {
                    this.fetch(this.page.current - 1)
                  }, 2000)
                }
              })
              .finally(() => {
                this.loading = false
                this.$nextTick(() => {
                  this.$refs.table?.doLayout()
                })
                callback?.(this.getData())
              })
        }, debounce)
      })
    },
    handleCurrent(val) {
      this.multipleSelection = []
      $emit(this, 'selection-change', [])
      this.$refs?.table?.clearSelection()
      this.fetch(val) //主要为了换页 清空选中数据
    },
    nodeChecked(tags) {
      this.tags = tags
      this.fetch(1)
    },
    handleSelectionChange(val) {
      this.multipleSelection = val
      $emit(this, 'selection-change', val)
    },
    showClassify(tagList) {
      this.$refs.classify.show(tagList)
    },
    getData() {
      return this.list
    },
    clearSelection() {
      this.$refs?.table?.clearSelection()
    }
  },
  emits: ['sort-change', 'classify-submit', 'selection-change']
}
</script>

